    public void Submit(){
        
        String N=Playername.getText();
        String B = Playerrating.getText();
        String R=Playerteam.getText();
        if(N.length()==0){
            JOptionPane.showMessageDialog(this, "Specify Player Name");
            Playername.requestFocus();
        }
        else if(!N.matches("^[a-zA-Z0-9_\\s]*$")){
            JOptionPane.showMessageDialog(this, "Player Name Should contain Alphabets ,numbers and white spaces only ");
            Playername.requestFocus();
       }
        else if(R.length()==0){
            JOptionPane.showMessageDialog(this, "Specify Team Name");
            Playername.requestFocus();
        }
        else if(!R.matches("^[a-zA-Z0-9_\\s]*$")){
            JOptionPane.showMessageDialog(this, "Team Name Should contain Alphabets ,numbers and white spaces only ");
            Playername.requestFocus();
       }
        else if(B.length()==0){
            JOptionPane.showMessageDialog(this,"Specify Player ratinf ");
            Playerrating.requestFocus();
        }
        else if( (!checkIfNumber(B)) || (Long.parseLong(B)<=0.99) ||(Long.parseLong(B)>=10.1) ){
            JOptionPane.showMessageDialog(this,"Rating Mustbe a number    between 1 and 10 ");
            Playerrating.requestFocus();
        }
        else { 
            int count=1;
            int countTeam=0;
            dbhandler db=new dbhandler();
            try{
                ResultSet r = db.st.executeQuery("SELECT COUNT(*) FROM team");
                r.next();
                count= r.getInt(1)+1;
                r.close(); 
            }
            catch(Exception e){
                System.out.println(e);
            JOptionPane.showMessageDialog(this,"Error Occured \n\t["+e+"]");
            }
            try{
                ResultSet r = db.st.executeQuery("SELECT COUNT(*) FROM team where TEAMNAME='"+N+"'");
                r.next();
                countTeam= r.getInt(1);
                r.close(); 
            }
            catch(Exception e){
                System.out.println(e);
            JOptionPane.showMessageDialog(this,"Error Occured \n\t["+e+"]");
            }
            if(countTeam!=0){
                JOptionPane.showMessageDialog(this,"Team Name Already exist");
                Playername.requestFocus();
            }
            else{
try{
            db.st.executeUpdate("INSERT INTO team(TEAMNO,TEAMNAME,CURRENTBUDGET,TOTALPLAYERS) " + 
                "VALUES ('"+count+"','"+N+"', '"+B+"',0)");
             
            JOptionPane.showMessageDialog(this,"Team Added ");
            this.dispose();
            }
            catch(SQLException | HeadlessException e){
                System.out.println(e);
            JOptionPane.showMessageDialog(this,"Error Occured \n\t["+e+"]");
            }
        }
        }
        System.out.println(N);
        System.out.println(B);
        
        
    }
    
    
     public boolean checkIfNumber(String in) {
        
        try {
 
            Long.parseLong(in);
        
        } catch (NumberFormatException ex) {
            return false;
        }
        
        return true;
    }
